package Fundamentos;

public class Aula50_OperadoresRelacionais {
    public static void main(String[] args) {

        System.out.println(3 == 3); // ==
        System.out.println(3 > 4); // >
        System.out.println(3 >= 3); // >=
        System.out.println(3 < 7); // <
        System.out.println(30 <= 7); // <=
        System.out.println(30 != 7); // !=

    }
}
