package fundamentos;

public class Aula52_OperadoresUnarios {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;

        // POSFIX
        a++; // a = a + 1
        a--; // a = a - 1

        // PREFIX
        ++b; // b = b + 1
        --b; // b = b - 1

        System.out.println(a);
        System.out.println(b);
    }
}
