package EstruturasDeControle;

public class Aula70_WhileDeterminado {
    public static void main(String[] args) {

        int cont = 0;

        while (cont <= 10){
            System.out.println("i = " + cont);
            cont++;
        }
        // Usamos o While quando precisamos repetir um codigo e nao sabemos quantas vezes repetir, ou seja, nÃ£o sabemos
        // quantas vezes ele vai rodar esse loop.

    }
}
